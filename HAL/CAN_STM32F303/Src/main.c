/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
	/* MCU Configuration--------------------------------------------------------*/
	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();
	/* Configure the system clock */
	SystemClock_Config();

	/* Initialize all configured peripherals */
	MX_GPIO_Init();

	HAL_CAN_vInit(&initConfig,&CAN_BITIME);
	HAL_CAN_vDebugMode(&initConfig);

	HAL_CAN_FILTER_CONFIG(&filterxConfig);

	HAL_CAN_start();

	uint8_t status;
	uint8_t DATA[8]={'A'};
	uint8_t MAILBOX_CODE;
	uint8_t RX_FRAMES_NUM;
	uint8_t RX_DATA[8];

	HAL_CAN_pu8TXFRAME(&initConfig,&TX_FRAME,DATA, &MAILBOX_CODE);

	do
	{
		HAL_CAN_p8CHECK_TXCOMPLETION(&status,MAILBOX_CODE);

	}while(status!=0);


	do
	{
		HAL_CAN_RX_FRAMES_NUM(FIFO0,&RX_FRAMES_NUM);

	}while(RX_FRAMES_NUM==0);

	HAL_CAN_psRXFRAME(&RX_FRAME,RX_DATA,FIFO0);


  while (1)
  {

  }
}

